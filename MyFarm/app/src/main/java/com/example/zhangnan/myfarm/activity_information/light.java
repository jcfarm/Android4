package com.example.zhangnan.myfarm.activity_information;

public class light {
	
	private int id;
	private float c;
	private float ph;
	private float lux;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public float getC() {
		return c;
	}
	public void setC(float c) {
		this.c = c;
	}
	public float getPh() {
		return ph;
	}
	public void setPh(float ph) {
		this.ph = ph;
	}
	public float getLux() {
		return lux;
	}
	public void setLux(float lux) {
		this.lux = lux;
	}

}
